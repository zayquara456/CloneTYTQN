<h3>Lá chắn an toàn</h3>
<p>Trong trường hợp không tránh khỏi va chạm, công nghệ của Nissan giúp giảm thương tích và thiệt hại bằng cách kích hoạt hệ thống phanh, giảm chấn cho hành khách, và áp dụng các biện pháp khác.</p>
</div>
<div class="item">
<p><img class="alignnone size-full wp-image-64" src="http://nissanhadong.com.vn/wp-content/uploads/2015/03/technology-safety-4.jpg" alt="technology-safety-4" /></p>
<h3>Cấu trúc Zone Body</h3>
<p>Cấu trúc Zone Body giúp nâng cao sự an toàn cho khoang lái và giảm nhẹ tác động đối với vùng đầu và chân của người đi bộ.</p>
</div>
<div class="item">
