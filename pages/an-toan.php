<h3>Bảo vệ môi trường</h3>
<p>Triết lý môi trường của Nissan có thể được diễn tả là, “một sự cộng sinh gắn bó giữa con người, xe hơi và thiên nhiên.” Đó là ý tưởng của chúng tôi cho một xã hội năng động bền vững và là mục tiêu của chúng tôi hướng tới tương lai.</p>
<p>Chúng tôi phát động chương trình hành động Nissan Xanh với những tiêu chí rõ ràng để đạt tới mục tiêu, và chúng tôi luôn luôn nỗ lực phối hợp cùng nhau để giảm ô nhiễm môi trường trong từng vòng đời sản phẩm cũng như trong mọi họat động kinh doanh của chúng tôi.</p>
</div>
