<h3>Công nghệ hỗ trợ leo dốc (HSA)</h3>
<p>HSA sẽ giúp cho bạn không bị trượt về sau trong khoảng thời gian vài giây khi chân bạn chuyển từ bàn đạp phanh sang nhấn ga tăng tốc. Bạn cũng không phải giữ phanh tay để xe không bị tụt dốc.</p>
</div>
<div class="item">
<p><img class="alignnone size-full wp-image-66" src="http://nissanhadong.com.vn/wp-content/uploads/2015/03/technology-safety-6.jpg" alt="technology-safety-6" /></p>
<h3>Công nghệ hỗ trợ đổ đèo</h3>
<p>Công nghệ hỗ trợ đổ đèo (HDC) đảm bảo xe luôn ở tốc độ cho phép, đồng thời trợ giúp việc điều khiển phanh khi đang xuống dốc , hệ thống này cho phép người lái tập trung hoàn toàn vào tay lái mà không cần rà thắng.</p>
