			<p><b>Thủ tục Đăng ký và Đăng kiểm xe</p>
<p></b></p>
<p>Với sự hỗ trợ tốt nhất từ Totota Quảng Ninh, khách hàng sử dụng xe Totota sẽ có trải nghiệm thú vị và tự tin khi tự thực hiện các công việc Đăng ký – Đăng kiểm xe (*).</p>
<p><strong>Qui định về Hồ sơ xe và Pháp nhân<br />
</strong></p>
<p><em><u>Hồ sơ xe bao gồm:<br />
</u></em></p>
<ul>
<li>Hóa đơn Giá trị gia tăng (VAT) do Toyota Việt Nam cấp (bản copy)</li>
<li>Hóa đơn GTGT do Totota Quảng Ninh cấp (đại lý ủy quyền chính thức bán xe Toyota) (bản gốc)</li>
<li>Phiếu kiểm tra chất lượng xuất xưởng (Tờ khai nhập khẩu hàng hóa nếu là xe nhập khẩu) (bản gốc)</li>
</ul>
<p><u><em>Giấy tờ Pháp nhân:<br />
</em></u></p>
<p><em>+ Đối với Cá nhân:</em></p>
<ul>
<li> Hộ khẩu + Chứng minh nhân dân (bản gốc, bản copy)</li>
</ul>
<p><em>+ Đối với tổ chức: Công ty, Văn phòng đại diện, Tổ chức nước ngoài:</em></p>
<ul>
<li>Giấy chứng nhận đăng ký kinh doanh, hoặc Giấy phép đầu tư, hoặc Giấp phép thành lập Văn phòng đại diện (bản copy công chứng)</li>
<li>Giấy giới thiệu cho cán bộ/nhân viên</li>
</ul>
<p><strong>Quy trình thủ tục Đăng ký – Đăng kiểm xe</strong></p>
<p><strong>Xin lưu ý:<br />
</strong></p>
<ul>
<li><strong>Tờ khai lệ phí trước bạ:</strong> Quý khách có thể lấy tại Chi cục thuế tại nơi quý khách đăng ký hộ khẩu thường trú nếu là cá nhân; hoặc nơi Đăng ký đặt trụ sở (nếu là tổ chức).</li>
<li><strong>Tờ khai đăng ký xe:</strong> Quý khách có thể lấy tại Phòng CSGT đăng ký xe ôtô tại nơi quý khách đăng ký hộ khẩu thường trú nếu là cá nhân; hoặc nơi Đăng ký đặt trụ sở (nếu là tổ chức).</li>
<li><strong>Đóng thuế trước bạ</strong>: Quý khách đóng thuế tại Chi cục thuế Quận &#8211; Huyện, tại nơi quý khách đăng ký hộ khẩu thường trú nếu là cá nhân; hoặc nơi Đăng ký đặt trụ sở (nếu là tổ chức).</li>
<li><strong>Đăng ký biển số xe:</strong> Quý khách đăng ký xe tại Đội đăng ký xe &#8211; Phòng CSGT Tỉnh, Thành phố.</li>
<li><strong>Đăng kiểm lưu hành xe:</strong> tại các trạm đăng kiểm trên toàn quốc.</li>
</ul>
<p>(*) Tư vấn Bán hàng chuyên nghiệp của Totota Quảng Ninh luôn sẵn sàng tư vấn và hỗ trợ Quý khách hàng thực hiện việc Đăng ký – Đăng kiểm lưu hành xe Toyota. Đặc biệt, đối với Quý khách hàng đăng ký xe tại Hà Nội sẽ được hỗ trợ trực tiếp và cung cấp Tờ khai, nếu được yêu cầu.</p>
