<h3>Chất lượng sản phẩm</h3>
<p>Chất lượng sản phẩm chính là ở chỗ khách hàng cảm thấy thoải mái và yên tâm khi lái xe trong một thời gian dài. Để đạt được những cải tiến thật sự về chất lượng sản phẩm, Nissan đã đặt ra những tiêu rõ ràng như: Giành được niềm tin của khách hàng trên toàn thế giới, Dẫn đầu về chất lượng trong nhận thức của khách hàng và trở thành nhà sản xuất ô tô được tin cậy nhất trên thế giới.</p>
