<h3>Cảm nhận chất lượng</h3>
<p>Nissan kiêu hãnh với sự hiện diện tiên phong trong lĩnh vực Chất lượng cảm nhận được với phương châm: Không ngừng nâng cao chất lượng thiết kế, Thúc đẩy quá trình sản xuất với chất lượng cao hơn và nhanh chóng giảm tỷ lệ máy hỏng xuống còn một nửa.</p>
