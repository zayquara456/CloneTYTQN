<p style="text-align: justify;"><sub>&#8211; Mòn hoặc rách thông thường, bao gồm cả tiếng kêu, nứt, rộp hoặc xước.</sub></p>
<p style="text-align: justify;"><sub>&#8211; Đá văng, hóa chất (mưa a-xít), nhựa cây, muối, mưa đá,bão, bão cát, ánh sáng, lũ lụt hoặc các điều kiện do môi trường khác.</sub></p>
