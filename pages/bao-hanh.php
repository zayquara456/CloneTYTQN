II. Những gì không được bảo hành<br />
</sub></p>
<p style="text-align: justify;"><sub>&#8211; Để đảm bảo quyền lợi của khách hàng thông qua dịch vụ mà Nissan Hà Đông mang lại là tốt nhất.Bên cạnh những hạng mục được bảo hành, Nissan Hà Đông xin lưu ý với quý khách những hạng mục không nằm trong chế độ bảo hành.</sub></p>
<ol style="text-align: justify;">
<li><sub>Lốp xe được lắp trên xe NISSAN mới mà không phải là nguyên bản (được lắp bởi Đại Lý không thuộc ủy quyền của NISSAN ) thì sẽ không được nhà sản xuất lốp bảo hành.</sub></li>
<li><sub>Tất cả các phụ kiện hoặc thiết bị không chính hiệu của NISSAN và được lắp bởi đại lý không phải của NISSAN sẽ không được bảo hành.</sub></li>
<li><sub>Bất cứ những phụ tùng hoặc tiền công phát sinh cùng với những yêu cầu hay khuyến cáo trong dịch vụ bảo dưỡng, được trình bày trong cuốn HƯỚNG DẪN SỬ DỤNG và PHẦN BẢO DƯỠNG của cuốn sách này.</sub></li>
<li><sub>Dịch vụ bảo dưỡng thông thường như điều chỉnh động cơ, rửa xe và đánh bóng, cân bằng bánh xe và điều chỉnh góc đặt bánh xe, điều chỉnh đèn pha, thay thế các bộ lọc, lắp gạt mưa kính lái, pin điều khiển từ xa,bóng đèn, bugi, dây đai dẫn động, chất bôi trơn và nước làm mát, má phanh, guốc phanh, trống phanh, đĩa phanh và đĩa li hợp.</sub></li>
<li><sub>Những hư hỏng hoặc lỗi gây ra do:<br />
&#8211; Sử dụng sai,tai nạn, bị trộm cắp hoặc cháy nổ ( Cách sử dụng đúng được trình bày trong cuốn HƯỚNG DẪN SỬ DỤNG )<br />
&#8211; Dùng sai loại nhiên liệu, dầu, nước làm mát hoặc chất bôi trơn hoặc do chúng bị bẩn<br />
&#8211; Thực hiện bảo dưỡng không đầy đủ như được trình bày trong cuốn</sub></li>
</ol>
